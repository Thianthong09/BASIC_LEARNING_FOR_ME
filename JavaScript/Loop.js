// for(ຄ່າເລີ່ມຕົ້ນຂອງຕົວແປ;ເງື່ອນໄຂ;ປ່ຽນແປງຄ່າຂອງຕົວແປ){
//     ຄຳສັ່ງເມື່ອເງື່ອນໄຂເປັນ True;
// }

for(let i=1;i<=10;i++){
    console.log("Hello");
}

// ---------------------------------------

let noy = 1;

do {
    console.log(noy);
    noy++;
} while (noy<=5);

