// ເພື່ອຈະໄປເຮັດຄຳສັ່ງອືີ່ນນອກ Loop

// continue ຄຳສັ່ງນີ້ຈະເຮັດໃຫ້ຢຸດການເຮັດວຽກແລ້ວຍ້ອນກັບໄປເລີ່ມຕົ້ນການເຮັດວຽກທີ່ຕົ້ນ Loop ໃໝ່

// ---------------------------------------

let count=1;

while(count<=3){
    console.log("Hello");
    if(count==4){
        break;
    }
    count++;
}

