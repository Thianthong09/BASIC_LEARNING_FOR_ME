//  - While ໃຊ້ໃນກໍລະນີທີ່ບໍ່ຮູ້ຈຳນວນຮອບ
//  - do..while ໃຊ້ໃນກໍລະນີທີ່ຢາກໃຫ້ລອງເຮັດກ່ອນ 1 ຮອບ ແລ້ວເຮັດໄປເລື່ອຍໆຈົນເທົ່າກັບເງື່ອນໄຂທີ່ເປັນ true